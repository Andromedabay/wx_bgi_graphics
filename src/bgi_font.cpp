#include "bgi_font.h"
#include "bgi_draw.h"
#include "bgi_state.h"

#include <array>
#include <cstdint>

namespace bgi
{

    // ---------------------------------------------------------------------------
    // 5×7 pixel font bitmaps
    // Each row is a 5-bit mask (bits 4..0 = columns left to right).
    // ---------------------------------------------------------------------------

    const std::array<std::uint8_t, 7> &glyphRows(unsigned char c)
    {
        static const std::array<std::uint8_t, 7> kUnknown = {0x1F, 0x11, 0x02, 0x04, 0x08, 0x11, 0x1F};
        static const std::array<std::uint8_t, 7> kSpace = {0, 0, 0, 0, 0, 0, 0};
        static const std::array<std::uint8_t, 7> kDash = {0, 0, 0, 0x1F, 0, 0, 0};
        static const std::array<std::uint8_t, 7> kUnderscore = {0, 0, 0, 0, 0, 0, 0x1F};
        static const std::array<std::uint8_t, 7> kPeriod = {0, 0, 0, 0, 0, 0x0C, 0x0C};
        static const std::array<std::uint8_t, 7> kColon = {0, 0x0C, 0x0C, 0, 0x0C, 0x0C, 0};
        static const std::array<std::uint8_t, 7> kSlash = {0x01, 0x02, 0x04, 0x08, 0x10, 0, 0};

        static const std::array<std::array<std::uint8_t, 7>, 10> kDigits = {{
            {0x0E, 0x11, 0x13, 0x15, 0x19, 0x11, 0x0E}, // 0
            {0x04, 0x0C, 0x04, 0x04, 0x04, 0x04, 0x0E}, // 1
            {0x0E, 0x11, 0x01, 0x02, 0x04, 0x08, 0x1F}, // 2
            {0x1E, 0x01, 0x01, 0x0E, 0x01, 0x01, 0x1E}, // 3
            {0x02, 0x06, 0x0A, 0x12, 0x1F, 0x02, 0x02}, // 4
            {0x1F, 0x10, 0x10, 0x1E, 0x01, 0x01, 0x1E}, // 5
            {0x0E, 0x10, 0x10, 0x1E, 0x11, 0x11, 0x0E}, // 6
            {0x1F, 0x01, 0x02, 0x04, 0x08, 0x08, 0x08}, // 7
            {0x0E, 0x11, 0x11, 0x0E, 0x11, 0x11, 0x0E}, // 8
            {0x0E, 0x11, 0x11, 0x0F, 0x01, 0x01, 0x0E}, // 9
        }};

        static const std::array<std::array<std::uint8_t, 7>, 26> kLetters = {{
            {0x0E, 0x11, 0x11, 0x1F, 0x11, 0x11, 0x11}, // A
            {0x1E, 0x11, 0x11, 0x1E, 0x11, 0x11, 0x1E}, // B
            {0x0E, 0x11, 0x10, 0x10, 0x10, 0x11, 0x0E}, // C
            {0x1E, 0x11, 0x11, 0x11, 0x11, 0x11, 0x1E}, // D
            {0x1F, 0x10, 0x10, 0x1E, 0x10, 0x10, 0x1F}, // E
            {0x1F, 0x10, 0x10, 0x1E, 0x10, 0x10, 0x10}, // F
            {0x0E, 0x11, 0x10, 0x10, 0x13, 0x11, 0x0E}, // G
            {0x11, 0x11, 0x11, 0x1F, 0x11, 0x11, 0x11}, // H
            {0x0E, 0x04, 0x04, 0x04, 0x04, 0x04, 0x0E}, // I
            {0x01, 0x01, 0x01, 0x01, 0x11, 0x11, 0x0E}, // J
            {0x11, 0x12, 0x14, 0x18, 0x14, 0x12, 0x11}, // K
            {0x10, 0x10, 0x10, 0x10, 0x10, 0x10, 0x1F}, // L
            {0x11, 0x1B, 0x15, 0x15, 0x11, 0x11, 0x11}, // M
            {0x11, 0x19, 0x15, 0x13, 0x11, 0x11, 0x11}, // N
            {0x0E, 0x11, 0x11, 0x11, 0x11, 0x11, 0x0E}, // O
            {0x1E, 0x11, 0x11, 0x1E, 0x10, 0x10, 0x10}, // P
            {0x0E, 0x11, 0x11, 0x11, 0x15, 0x12, 0x0D}, // Q
            {0x1E, 0x11, 0x11, 0x1E, 0x14, 0x12, 0x11}, // R
            {0x0F, 0x10, 0x10, 0x0E, 0x01, 0x01, 0x1E}, // S
            {0x1F, 0x04, 0x04, 0x04, 0x04, 0x04, 0x04}, // T
            {0x11, 0x11, 0x11, 0x11, 0x11, 0x11, 0x0E}, // U
            {0x11, 0x11, 0x11, 0x11, 0x11, 0x0A, 0x04}, // V
            {0x11, 0x11, 0x11, 0x15, 0x15, 0x15, 0x0A}, // W
            {0x11, 0x11, 0x0A, 0x04, 0x0A, 0x11, 0x11}, // X
            {0x11, 0x11, 0x0A, 0x04, 0x04, 0x04, 0x04}, // Y
            {0x1F, 0x01, 0x02, 0x04, 0x08, 0x10, 0x1F}, // Z
        }};

        // Fold lower-case to upper-case
        if (c >= 'a' && c <= 'z')
        {
            c = static_cast<unsigned char>(c - 'a' + 'A');
        }

        if (c >= 'A' && c <= 'Z')
        {
            return kLetters[static_cast<std::size_t>(c - 'A')];
        }
        if (c >= '0' && c <= '9')
        {
            return kDigits[static_cast<std::size_t>(c - '0')];
        }

        switch (c)
        {
        case ' ':
            return kSpace;
        case '-':
            return kDash;
        case '_':
            return kUnderscore;
        case '.':
            return kPeriod;
        case ':':
            return kColon;
        case '/':
            return kSlash;
        default:
            return kUnknown;
        }
    }

    void drawGlyph(int x, int y, unsigned char c)
    {
        const auto &rows = glyphRows(c);
        for (int py = 0; py < 7; ++py)
        {
            const std::uint8_t row = rows[static_cast<std::size_t>(py)];
            for (int px = 0; px < 5; ++px)
            {
                const std::uint8_t mask = static_cast<std::uint8_t>(1U << (4 - px));
                if ((row & mask) != 0U)
                {
                    setPixel(x + px, y + py, gState.currentColor);
                }
            }
        }
    }

} // namespace bgi
